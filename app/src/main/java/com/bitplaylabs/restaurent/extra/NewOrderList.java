public class NewOrderList {


    private String kot;
    private String item_name;
    private String quantity;
    private String table_no;
    private String order_time;
    private String time_elapsed;
    private String order_taken_by;

    public String getKot() {
        return kot;
    }

    public void setKot(String kot) {
        this.kot = kot;
    }

    public String getItem_name() {
        return item_name;
    }

    public void setItem_name(String item_name) {
        this.item_name = item_name;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getTable_no() {
        return table_no;
    }

    public void setTable_no(String table_no) {
        this.table_no = table_no;
    }

    public String getOrder_time() {
        return order_time;
    }

    public void setOrder_time(String order_time) {
        this.order_time = order_time;
    }

    public String getTime_elapsed() {
        return time_elapsed;
    }

    public void setTime_elapsed(String time_elapsed) {
        this.time_elapsed = time_elapsed;
    }

    public String getOrder_taken_by() {
        return order_taken_by;
    }

    public void setOrder_taken_by(String order_taken_by) {
        this.order_taken_by = order_taken_by;
    }
}
